import rateLimit from 'express-rate-limit';

export const authLoginRateLimit = rateLimit({
  windowMs: 15 * 60 * 1000,
  max: 30,
  standardHeaders: true,
  legacyHeaders: false,
  message: {
    success: false,
    message: 'Muitas tentativas de autenticacao. Tente novamente em alguns minutos.',
    code: 'TOO_MANY_REQUESTS',
  },
});
